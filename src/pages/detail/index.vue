<script>
import ImageList from "./ImageList/ImageList";
import Zoom from "./Zoom/Zoom";

//通过辅助函数获取数据
import { mapState } from "vuex";
export default {
  name: "Detail",
  components: {
    ImageList,
    Zoom,
  },
  data() {
    return {
      skuNum: 1,
    };
  },
  mounted() {
    this.$store.dispatch("detail/getDetail");
  },
  computed: {
    ...mapState("detail", ["detail"]),
  },
  methods: {
    handleClick(content, row) {
      row.forEach((element) => {
        element.isChecked = 0;
      });
      content.isChecked = 1;
    },
    changeSku(event) {
      const value = event.target.value;
      // 如果输入的内容不是正整数，就清空文本框
      if (!/^[1-9][0-9]*$/.test(value)) {
        this.skuNum = 1;
      }
    },
    addToCart() {
      // 使用路由信息进行跳转
      this.$router.push({
        name: "addShopCar",
        params: { skuNum: this.skuNum },
      });
    },
  },
};
</script>

